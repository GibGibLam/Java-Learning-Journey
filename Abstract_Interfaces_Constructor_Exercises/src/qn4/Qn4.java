package qn4;

public abstract class Qn4 {

	private String name;
	 private String member;
	


	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}



	public String getMember() {
		return member;
	}



	public void setMember(String member) {
		this.member = member;
	}



	public Qn4(String name, String member) {
		super();
		this.name = name;
		this.member = member;
	}
}



	
